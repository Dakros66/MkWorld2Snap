import { derived, writable } from 'svelte/store';

export type Locale = 'en' | 'es' | 'fr' | 'it' | 'pt' | 'de';

export const localeOptions: Array<{ code: Locale; label: string }> = [
  { code: 'en', label: 'English' },
  { code: 'es', label: 'Español' },
  { code: 'fr', label: 'Français' },
  { code: 'it', label: 'Italiano' },
  { code: 'pt', label: 'Português' },
  { code: 'de', label: 'Deutsch' },
];

const supported: Locale[] = ['en', 'es', 'fr', 'it', 'pt', 'de'];

function detectLocale(): Locale {
  try {
    const saved = localStorage.getItem('mkworld2snap-locale') as Locale | null;
    if (saved && supported.includes(saved)) return saved;
    const pc = navigator.language.slice(0, 2).toLowerCase() as Locale;
    if (supported.includes(pc)) return pc;
  } catch {
    // keep default
  }
  return 'en';
}

export const locale = writable<Locale>(detectLocale());

locale.subscribe((value) => {
  try {
    localStorage.setItem('mkworld2snap-locale', value);
    document.documentElement.lang = value;
  } catch {
    // localStorage can be blocked; the in-memory language still works.
  }
});

